package com.cnnp.race.demo.entity;

public class Game {
	
	String id;
	String game_no;
	String start_time;
	String location;
	String comment;
	String judge_names;
	String judge_nos;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getGame_no() {
		return game_no;
	}
	public void setGame_no(String game_no) {
		this.game_no = game_no;
	}
	public String getStart_time() {
		return start_time;
	}
	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getJudge_names() {
		return judge_names;
	}
	public void setJudge_names(String judge_names) {
		this.judge_names = judge_names;
	}
	public String getJudge_nos() {
		return judge_nos;
	}
	public void setJudge_nos(String judge_nos) {
		this.judge_nos = judge_nos;
	}

	
	
}
