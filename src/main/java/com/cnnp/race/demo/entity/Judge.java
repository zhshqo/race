package com.cnnp.race.demo.entity;

public class Judge {

	String id;
	String judge_no;
	String judge_name;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getJudge_no() {
		return judge_no;
	}
	public void setJudge_no(String judge_no) {
		this.judge_no = judge_no;
	}
	public String getJudge_name() {
		return judge_name;
	}
	public void setJudge_name(String judge_name) {
		this.judge_name = judge_name;
	}
	
}
